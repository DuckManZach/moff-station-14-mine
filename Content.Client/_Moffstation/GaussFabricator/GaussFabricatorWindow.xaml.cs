using Content.Client.UserInterface.Controls;
using Content.Shared._Moffstation.GaussFabricator;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._Moffstation.GaussFabricator;

[GenerateTypedNameReferences]
public sealed partial class GaussFabricatorWindow : FancyWindow
{
    public event Action<float>? OnAdjustDrawRate;
    public event Action<bool>? OnToggle;

    private readonly ButtonGroup _toggleGroup = new();

    public GaussFabricatorWindow()
    {
        RobustXamlLoader.Load(this);

        DecreaseLargeButton.OnPressed += _ => OnAdjustDrawRate?.Invoke(-10_000f);
        DecreaseSmallButton.OnPressed += _ => OnAdjustDrawRate?.Invoke(-1_000f);
        IncreaseSmallButton.OnPressed += _ => OnAdjustDrawRate?.Invoke(1_000f);
        IncreaseLargeButton.OnPressed += _ => OnAdjustDrawRate?.Invoke(10_000f);

        OnButton.Group = _toggleGroup;
        OffButton.Group = _toggleGroup;
        OnButton.OnPressed += _ => OnToggle?.Invoke(true);
        OffButton.OnPressed += _ => OnToggle?.Invoke(false);
    }

    public void UpdateState(GaussFabricatorBuiState state)
    {
        DrawRateLabel.Text = FormatPower(state.ConfiguredDrawRate);
        ReceivedLabel.Text = FormatPower(state.ReceivedPower);

        var progress = Math.Clamp(state.Progress, 0f, 1f);
        OutputProgressBar.Value = progress;
        OutputProgressBar.ForegroundStyleBoxOverride = new StyleBoxFlat(ProgressColor(progress));

        DrawLevelBar.SetValues(state.MaxChargeRate, state.ReceivedPower, state.ConfiguredDrawRate);

        if (state.IsOn)
            OnButton.Pressed = true;
        else
            OffButton.Pressed = true;
    }

    private static Color ProgressColor(float t)
    {
        if (t < 0.5f)
            return Color.InterpolateBetween(Color.Red, Color.Lime, t * 2f);

        return Color.InterpolateBetween(Color.Lime, Color.Cyan, (t - 0.5f) * 2f);
    }

    private static string FormatPower(float watts)
    {
        return watts switch
        {
            >= 1_000_000f => $"{watts / 1_000_000f:F1} MW",
            >= 1_000f => $"{watts / 1_000f:F1} kW",
            _ => $"{watts:F0} W",
        };
    }
}
